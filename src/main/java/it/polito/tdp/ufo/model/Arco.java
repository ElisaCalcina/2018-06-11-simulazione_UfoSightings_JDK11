package it.polito.tdp.ufo.model;

public class Arco {
	String stato1;
	String stato2;
	
	
	public Arco(String stato1, String stato2) {
		super();
		this.stato1 = stato1;
		this.stato2 = stato2;
	}


	public String getStato1() {
		return stato1;
	}


	public void setStato1(String stato1) {
		this.stato1 = stato1;
	}


	public String getStato2() {
		return stato2;
	}


	public void setStato2(String stato2) {
		this.stato2 = stato2;
	}
	
	

}
